void setIO(string name = "") {
    cin.tie(0)->sync_with_stdio(0); // see /general/fast-io
    if (sz(name)) {
        freopen((name + ".in").c_str(), "r", stdin); // see /general/input-output
        freopen((name + ".out").c_str(), "w", stdout);
    }
}

int main() {
    setIO("");
    int n;cin >> n;
    vector<int> a(n);
    for(auto  &e:a) cin >> e;
    int ans=0;
    for(int i=0;i<n;i++){
        for(int j=i;j<n;j++){
            int sum=0;
            for(int k=i;k<=j;k++){
                sum+=a[k];
            }
            int found = 0;
            for(int k=i;k<=j;k++){
                if(a[k]*(j-i+1) == sum) found = 1;
            }
            if(found) ans++;
        }
    }
    cout << ans;
    return 0;
}
